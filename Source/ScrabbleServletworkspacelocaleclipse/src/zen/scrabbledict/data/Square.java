package zen.scrabbledict.data;

/**
 * This enumerates the square types.
 * @author flszen
 */
public enum Square {

    /**
     * A normal square.
     */
    NORMAL,
    /**
     * A double letter score square.
     */
    DOUBLE_LETTER,
    /**
     * A double word score square.
     */
    DOUBLE_WORD,
    /**
     * A triple letter score square.
     */
    TRIPLE_LETTER,
    /**
     * A triple word score square.
     */
    TRIPLE_WORD,
    /**
     * The center square.
     */
    CENTER
}
